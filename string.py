name = "Parth Gulati"

print("Name is:", name)

# prints first character of string
print(name[0])

# prints 3rd to 5th character
print(name[2:5])

# prints string starting from 3rd character
print(name[2:])

# prints string two times
print(name * 2)

# prints concatenated string
print(name + " Hello")
